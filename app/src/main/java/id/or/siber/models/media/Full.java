package id.or.siber.models.media;

public class Full{
	private String file;
	private String mimeType;
	private int width;
	private String sourceUrl;
	private int height;

	public String getFile(){
		return file;
	}

	public String getMimeType(){
		return mimeType;
	}

	public int getWidth(){
		return width;
	}

	public String getSourceUrl(){
		return sourceUrl;
	}

	public int getHeight(){
		return height;
	}
}
