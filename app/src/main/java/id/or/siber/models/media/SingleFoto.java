package id.or.siber.models.media;

public class SingleFoto{
	private String file;
	private String mimeType;
	private int heightQuery;
	private int width;
	private int widthQuery;
	private int filesize;
	private String sourceUrl;
	private int height;

	public String getFile(){
		return file;
	}

	public String getMimeType(){
		return mimeType;
	}

	public int getHeightQuery(){
		return heightQuery;
	}

	public int getWidth(){
		return width;
	}

	public int getWidthQuery(){
		return widthQuery;
	}

	public int getFilesize(){
		return filesize;
	}

	public String getSourceUrl(){
		return sourceUrl;
	}

	public int getHeight(){
		return height;
	}
}
